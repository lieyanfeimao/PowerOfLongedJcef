package com.xuanyimao.polj.index.bean;

/**
 * @Description: 配置
 * @author liuming
 */
public class ScpConfig {

	private String file;
	
	private String url;
	
	private String name;
	
	private String type;

	/**
	 * file
	 * @return file
	 */
	public String getFile() {
		return file;
	}

	/**
	 * 设置 file
	 * @param file file
	 */
	public void setFile(String file) {
		this.file = file;
	}

	/**
	 * url
	 * @return url
	 */
	public String getUrl() {
		return url;
	}

	/**
	 * 设置 url
	 * @param url url
	 */
	public void setUrl(String url) {
		this.url = url;
	}

	/**
	 * name
	 * @return name
	 */
	public String getName() {
		return name;
	}

	/**
	 * 设置 name
	 * @param name name
	 */
	public void setName(String name) {
		this.name = name;
	}

	/**
	 * type
	 * @return type
	 */
	public String getType() {
		return type;
	}

	/**
	 * 设置 type
	 * @param type type
	 */
	public void setType(String type) {
		this.type = type;
	}
}
